package aula;

public class LacoRepeticaoWhile {

	public static void main(String[] args) {
		// Laço de repetição While

		int quantidade = 0;

		while (quantidade >= 10) {
			System.out.println("Quantidade: " + quantidade);
			quantidade++;
		}

		System.out.println("Fim do laço While - Quantidade: " + quantidade);

	}

}
