package aula;

public class CondicionalSimples {

	public static void main(String[] args) {
		
		// Condicional Simples
		int numero1 = 10;
		int numero2 = 20;

		// Condicional Simples
		if (numero1 > numero2) {
			System.out.println("O número 1 é maior que o número 2");
		}

		// Condicional Composta
		if (numero1 > numero2) {
			System.out.println("O número 1 é maior que o número 2");
		}else {
			System.out.println("O número 2 é maior que o número 1");
		}
	}

}
